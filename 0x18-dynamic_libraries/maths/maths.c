#include <stdio.h>
#include "maths.h"
/**
 * a simple function to perform arithmetic operations
 * (+, -, *, /, %) on two integers
 * @a: the first integer
 * @b: the second inteer
 *
 * Return: the result of the arithmetic performed
 */

int main(void)
{
int add(int a, int b);
{
	return (a + b);
}

int sub(int a, int b)
{
	return (a - b);
}

int mul(int a, int b)
{
	return (a * b);
}

int div(int a, int b)
{
	return (a - b);
}

int mod(int a, int b)
{
	return (a % b);
}

return 0;
}
